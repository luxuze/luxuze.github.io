/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */
func preorderTraversal(root *TreeNode) []int {
	var answer []int
	var dfs func(*TreeNode)
	dfs = func(tn *TreeNode) {
		if tn == nil {
			return
		}
		answer = append(answer, tn.Val)
		dfs(tn.Left)
		dfs(tn.Right)
	}
	dfs(root)
	return answer
}

